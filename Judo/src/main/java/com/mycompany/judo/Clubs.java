    private void buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarActionPerformed
        // TODO add your handling code here:
        
        
      int club = 0;

        switch(clb.getSelectedItem().toString()){
                
                case "Budokan":
                    club = clubs.get(0).getId_club();
                break;
                
                case "Mederre":
                    club = clubs.get(1).getId_club();
                break;
                
                case "Ronnin":
                    club = clubs.get(2).getId_club();
                break;
                
                case "Tsukiru":
                    club = clubs.get(3).getId_club();
                break;
                
                case "Okami":
                    club = clubs.get(4).getId_club();
                break;
                
                case "Jushirokan":
                    club = clubs.get(5).getId_club();
                break;
                
                case "Bezana":
                    club = clubs.get(6).getId_club();
                break;
                
                case "Mislata":
                    club = clubs.get(7).getId_club();
                break;
            }
        
        
        if (entre.isSelected() && compe.isSelected()){
            JOptionPane.showMessageDialog(null, "SOLO PUEDES SELECIONAR UNO A LA VEZ");
        }else if (compe.isSelected()) {
            ICompetidorDAO iclubDAO = new CompetidorDAO();   
            List<Competidor> clubs = iclubDAO.clubs(club);
            
            DefaultTableModel modelo = new DefaultTableModel();
        
            modelo.addColumn("ID_CLUB");
            modelo.addColumn("NOMBRE");
            modelo.addColumn("APELLIDO");

            String[] datos = new String[4];
            //Datos a mostrar en el jTable
            clubs.forEach(clu -> {
                datos[0] = String.valueOf(clu.getId_club());
                datos[1] = clu.getNombre();
                datos[2] = clu.getApellido();
                modelo.addRow(datos);
            });
            
            tabla.setModel(modelo);
         
            
        } else if (entre.isSelected()){
            IEntrenadorDAO iclubDAO = new EntrenadorDAO();   
            List<Entrenador> clubs = iclubDAO.clubs(club);
            
            DefaultTableModel modelo = new DefaultTableModel();
        
            modelo.addColumn("ID_CLUB");
            modelo.addColumn("NOMBRE");
            modelo.addColumn("APELLIDO");

            String[] datos = new String[4];
            //Datos a mostrar en el jTable
            clubs.forEach(clu -> {
                datos[0] = String.valueOf(clu.getId_club());
                datos[1] = clu.getNombre();
                datos[2] = clu.getApellido();
                modelo.addRow(datos);
            });
            
            tabla.setModel(modelo) ;
        }else{
            JOptionPane.showMessageDialog(null, "DEBES SELECCIONAR COMPETIDOR O ENTRENADOR");
        }
 
        
    }//GEN-LAST:event_buscarActionPerformed

